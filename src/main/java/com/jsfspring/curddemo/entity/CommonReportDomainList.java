package com.jsfspring.curddemo.entity;

public class CommonReportDomainList {
	
	private int rowId;
	
	private String description;
	
	private String type;
	
	private double amtOrCount;

	public int getRowId() {
		return rowId;
	}

	public void setRowId(int rowId) {
		this.rowId = rowId;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public double getAmtOrCount() {
		return amtOrCount;
	}

	public void setAmtOrCount(double amtOrCount) {
		this.amtOrCount = amtOrCount;
	}
	
	

}
